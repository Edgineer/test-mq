<h3>Interpreting the Law </h3>

<p class="quote"><span class="quote">"Independence means you decide according to the law and the facts." </span> - Stephen Breyer</p>

<p>
    It is the Supreme Court's role to interpret the Constitution, serving as the final arbiter in questions of federal law and constitutional principles. 
    This responsibility requires justices to navigate complex legal and societal issues while remaining faithful to the text and spirit of the Constitution. 
    However, their interpretations are shaped by differing judicial philosophies.
</p>

<p class="thick">Living Constitution (Pragmatism)</p>
<p>
    Views the Constitution as a dynamic document that evolves to address contemporary issues. 
    It emphasizes adaptability to modern values and societal progress, and
    considers broader implications and practical consequences of rulings.
</p>

<p class="thick">Originalism</p>
<p><span class="quote">“The Constitution is a written instrument. As such its meaning does not alter. That which it meant when adopted it means now.” </span> ― Antonin Scalia</p>
<p>
    Interpreting the Constitution based on its meaning at the time it was written and ratified.
    It seeks to preserve the framers' intent, emphasizing stability and limiting judicial discretion.
</p>

<p class="thick">Case Study: 2nd Amendment</p>
<p>
    Originalists believe the Second Amendment protects an individual's right to own firearms for self-defense, as originally intended, with minimal restrictions.

    Advocates of the living Constitution argue it must be interpreted in the context of modern circumstances, balancing gun rights with public safety and addressing issues like gun violence.
</p>




